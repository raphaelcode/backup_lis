			A palavra Metafísica tem origem no grego e significa: meta= além + física ou matéria.
			Ou seja, é um estudo que busca o conhecimento das essências das coisas.<br>
			Este termo foi atribuído pelo filósofo Aristóteles, embora tenha deixado esse estudo a margem de suas investigações.<br>
			Mas essa ciência passou a ser organizada com o surgimento da Otonlogia, uma parte da metafísica que estuda o problema do SER, enquanto SER.<br>
