		<p>
		<br><br>
		A Craniopuntura é a Acupuntura realizada no crânio. O crânio é um micro-sistema, tendo uma representação de todo o corpo na cabeça e seus pontos podem ser estimulados como se estivéssemos estimulando os pontos de Acupuntura no corpo.<br>
		É uma técnica segura utilizada pela medicina oriental para tratar especialmente dores crônicas, porém a craniopuntura também é utilizada com sucesso em casos de limitação dos movimentos e alterações do sistema nervoso central.<br>
		Pode ser aplicada em pessoas de todas as idades. São vários os benefícios dessa técnica que dura cerca de 20 à 30 minutos por sessão, em alguns casos a dor é sedada no mesmo instante, aliviando a dor. O número de sessões depende de caso para caso.<br>
		Veja alguns exemplos:<br>
		Todas as dores e parestesias dos membros superiores, distensões musculares, bronquite, asma, dispneia, dores no trajeto dos nervos de origem cervical e outros.<br>
		Hérnia de disco, lumbago, bursites, reumatismos, artrites, coluna vertebral, nervo ciático.<br>
		Alergias, sinusite, rinite, obstruções nasal, glaucoma, problemas relacionados com os olhos em geral, enxaquecas, disfunções endócrinas, otites, cefaleias, paralisia facial, e etc.<br>
