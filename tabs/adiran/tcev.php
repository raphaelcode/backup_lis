			O mesmo ocorre quando a pessoa está sob tensão crônica, sua vitalidade será rebaixada.<br>
			Baseia-se no principio de que tudo pode se fazer novo novamente se assim o desejarmos, no principio do amor a si mesmo e aos outros, principalmente vivenciar o agora e que cada um é responsável por si mesmo, pelas dores e sofrimentos, pelas alegrias, riquezas e prosperidade, saúde em todos os aspectos.<br>
			A prática da Terapia corporal energética vibracional pode ser praticada por todos que desejam viver uma vida saudável, vibrante e livre das amarras que o impedem de seguir um caminho de sucesso seja ele pessoal, social ou profissional. Todos somos capazes de viver uma vida de intensa alegria, dentro de cada um de nós existe uma força admirável e que ao ser despertada nos torna capazes de superar a dor e a desilusão, mesmo que a princípio duvidemos disso.<br>
