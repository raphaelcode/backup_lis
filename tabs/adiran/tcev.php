			A Terapia Corporal Energética Vibracional tem um enfoque Holístico. Baseia -se num conjunto de técnicas milenares e modernas, as quais buscam harmonizar equilibrar e restaurar os campos energéticos dos indivíduos:físico- emocional-mental e espiritual. Oferece várias ferramentas para que cada um possa encontrar a si mesmo, fazer uma auto análise de sua vida, seus comportamentos, desejos, pensamentos e sentimentos. Ajuda a despertar a força interior, aumentar a energia psíquica e física para resolver-se internamente a curto, médio e até à longo prazo dependendo das situações vivenciadas por cada um individualmente e guardadas ao longo dos anos, meses ou dias.<br>
